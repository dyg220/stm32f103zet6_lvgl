void Delay_us(uint32_t us)
{
    uint32_t delay = us * 8;
    do {
        __NOP();
    } while(--delay);
}

void Delay_ms(uint32_t ms)
{
    uint32_t delay = ms * 8000;
    do {
        __NOP();
    } while(--delay);
}

void Delay_s(uint32_t s)
{
    while(s--)
    {
        Delay_ms(1000);
    }
}
